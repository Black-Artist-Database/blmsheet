        <h3 class="bad-subtitle">The beginning, how we started, our aims and goals</h3>

        <p>From its origin as a spreadsheet created by a group of like-minded music enthusiasts, B.A.D. was developed to further leverage Black artists, labels and producers in an accessible database format.</p>

        <p>The two key factors that contributed to the initial creation of B.A.D., then called Black Bandcamp, were the Black Lives Matter movement and the global Covid-19 pandemic. </p>

        <p>With the outcry after the murder of George Floyd and other Black people globally, Black Bandcamp was created as a response to the structural mistreatment of Black folk in the underground electronic industry. In addition to this, the whitewashing and eurocentrism of the industry functionally erased the culture in which this music is rooted - Black culture. </p>

        <p>Although it should not have taken a global pandemic for systemic racism to be acknowledged, the circumstances allowed further momentum to be built; this momentum was integral to the project expanding to become a widely embraced resource within the community. </p>

        <p>Another factor in the creation and early momentum of Black Bandcamp was ‘Bandcamp Fridays’, where Bandcamp waived their fees every first Friday of the month throughout 2020 and into 2021. This presented a prime opportunity for people to directly support Black artists monetarily. The highly exploitative digital and streaming music platform model became ever more apparent, which further spurred us to use Bandcamp as the primary platform to support Black artists. As the only available marketplace directly for supporting artists, the community began engaging with B.A.D. more actively on Bandcamp Fridays. </p>

        <p>By aggregating such a wealth of Black artists on a centralised platform, we highlight the boundless range of Black musicality and enable the Black voice to shine as a collective. We hope to give the power back to the community far and wide through user-submitted artist profiles, removing a layer of industry gatekeeping. By creating, maintaining, and publicising the database, the community surrounding B.A.D. is able to push back against the structural inequity present in the music industry. </p>

